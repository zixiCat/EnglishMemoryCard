import { mkdir, readdir, readFile, writeFile } from 'node:fs/promises';
import { basename, dirname, extname, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import process from 'node:process';

const scriptPath = fileURLToPath(import.meta.url);
const scriptDir = dirname(scriptPath);
const workspaceRoot = resolve(scriptDir, '..');
const defaultInputDir = resolve(workspaceRoot, 'apps/english-memory-card/content');
const outputFilePath = resolve(
  workspaceRoot,
  'apps/english-memory-card/src/app/data/generated-notes.ts'
);

const inputDir = normalizeInputDirectory(process.argv[2]);

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exitCode = 1;
});

async function main() {
  const markdownFiles = await collectMarkdownFiles(inputDir);

  if (markdownFiles.length === 0) {
    throw new Error(`No Markdown files were found in ${inputDir}.`);
  }

  const sections = [];

  for (const filePath of markdownFiles) {
    const markdown = await readFile(filePath, 'utf8');
    const relativeFilePath = normalizeSlashes(relative(inputDir, filePath));
    const fileSections = parseMarkdownSections(markdown, filePath, relativeFilePath);

    sections.push(...fileSections);
  }

  if (sections.length === 0) {
    throw new Error(
      'Markdown files were found, but no `##` headings started with a supported date.'
    );
  }

  await mkdir(dirname(outputFilePath), { recursive: true });

  const generatedModule = [
    '// This file is generated by npm run import:cards',
    `export const importedNoteSections = ${JSON.stringify(sections, null, 2)} as const;`,
    '',
  ].join('\n');

  await writeFile(outputFilePath, generatedModule, 'utf8');

  console.log(`Imported ${sections.length} cards from ${markdownFiles.length} Markdown files.`);
  console.log(`Wrote ${outputFilePath}`);
}

function normalizeInputDirectory(rawInputDir) {
  if (!rawInputDir) {
    return defaultInputDir;
  }

  if (/^[a-zA-Z]:\\/.test(rawInputDir)) {
    const driveLetter = rawInputDir[0].toLowerCase();
    const restPath = rawInputDir.slice(3).replace(/\\/g, '/');
    return `/mnt/${driveLetter}/${restPath}`;
  }

  return resolve(rawInputDir);
}

async function collectMarkdownFiles(directoryPath) {
  const directoryEntries = await readdir(directoryPath, { withFileTypes: true });
  const markdownFiles = [];

  for (const entry of directoryEntries) {
    const entryPath = resolve(directoryPath, entry.name);

    if (entry.isDirectory()) {
      const nestedFiles = await collectMarkdownFiles(entryPath);
      markdownFiles.push(...nestedFiles);
      continue;
    }

    if (/\.(md|markdown)$/i.test(entry.name)) {
      markdownFiles.push(entryPath);
    }
  }

  return markdownFiles.sort((left, right) => left.localeCompare(right));
}

function parseMarkdownSections(markdown, filePath, relativeFilePath) {
  const headings = Array.from(markdown.matchAll(/^##\s+(.+)$/gm));
  const sections = [];

  headings.forEach((headingMatch, index) => {
    const headingText = headingMatch[1].trim();
    const parsedHeading = parseDateHeading(headingText);

    if (!parsedHeading || headingMatch.index === undefined) {
      return;
    }

    const bodyStart = headingMatch.index + headingMatch[0].length;
    const bodyEnd = index + 1 < headings.length && headings[index + 1].index !== undefined
      ? headings[index + 1].index
      : markdown.length;
    const body = markdown.slice(bodyStart, bodyEnd).replace(/^\s+|\s+$/g, '');

    if (!body) {
      return;
    }

    sections.push({
      id: buildSectionId(relativeFilePath, parsedHeading.isoDate, sections.length + 1),
      fileName: basename(filePath),
      relativePath: relativeFilePath,
      date: parsedHeading.isoDate,
      title: parsedHeading.title || stripExtension(basename(filePath)),
      body,
      excerpt: buildExcerpt(body),
      wordCount: countWords(body),
    });
  });

  return sections;
}

function parseDateHeading(headingText) {
  const isoLikeMatch = headingText.match(
    /^(\d{4})([-/.])(\d{1,2})\2(\d{1,2})(.*)$/
  );

  if (isoLikeMatch) {
    const year = Number(isoLikeMatch[1]);
    const month = Number(isoLikeMatch[3]);
    const day = Number(isoLikeMatch[4]);

    if (!isValidDateParts(year, month, day)) {
      return null;
    }

    return {
      isoDate: `${year}-${pad(month)}-${pad(day)}`,
      title: cleanHeadingTail(isoLikeMatch[5]),
    };
  }

  const chineseDateMatch = headingText.match(
    /^(\d{4})年(\d{1,2})月(\d{1,2})日(.*)$/
  );

  if (chineseDateMatch) {
    const year = Number(chineseDateMatch[1]);
    const month = Number(chineseDateMatch[2]);
    const day = Number(chineseDateMatch[3]);

    if (!isValidDateParts(year, month, day)) {
      return null;
    }

    return {
      isoDate: `${year}-${pad(month)}-${pad(day)}`,
      title: cleanHeadingTail(chineseDateMatch[4]),
    };
  }

  return null;
}

function cleanHeadingTail(rawTail) {
  return rawTail.replace(/^[\s|:：-]+/, '').trim();
}

function isValidDateParts(year, month, day) {
  const candidateDate = new Date(year, month - 1, day);

  return (
    candidateDate.getFullYear() === year
    && candidateDate.getMonth() === month - 1
    && candidateDate.getDate() === day
  );
}

function buildSectionId(relativeFilePath, isoDate, index) {
  const safeFileStem = stripExtension(relativeFilePath)
    .normalize('NFKD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-zA-Z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .toLowerCase();

  return `${safeFileStem || 'note'}-${isoDate}-${index}`;
}

function buildExcerpt(body) {
  return body.replace(/\s+/g, ' ').trim().slice(0, 140);
}

function countWords(body) {
  return body.split(/\s+/).filter(Boolean).length;
}

function stripExtension(fileName) {
  return fileName.slice(0, -extname(fileName).length);
}

function pad(value) {
  return String(value).padStart(2, '0');
}

function normalizeSlashes(filePath) {
  return filePath.replace(/\\/g, '/');
}